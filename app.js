function renderBrowse() { applyFilters(); }

function applyFilters() {
  if (!allItems.length) return;

  const query   = (el('search-box')?.value  || '').toLowerCase().trim();
  const sort    = el('sort-select')?.value  || 'size-desc';
  const javOnly = el('jav-only')?.checked   || false;

  let items = allItems.filter(item => {
    if (javOnly && !item.is_jav) return false;
    if (query) {
      const hay = (item.name + ' ' + (item.bango || '') + ' ' +
                   (item.title || '') + ' ' + (item.actresses || []).join(' ')).toLowerCase();
      return hay.includes(query);
    }
    return true;
  });

  items.sort((a, b) => {
    switch (sort) {
      case 'size-desc':  return b.total_size - a.total_size;
      case 'size-asc':   return a.total_size - b.total_size;
      case 'name-asc':   return a.name.localeCompare(b.name);
      case 'name-desc':  return b.name.localeCompare(a.name);
      case 'series-asc': return (a.series || 'zzz').localeCompare(b.series || 'zzz');
      default:           return 0;
    }
  });

  filteredItems = items;
  setText('result-count', items.length);
  setText('result-size', bytesToHuman(items.reduce((s, i) => s + (i.total_size || 0), 0)));
  renderItemList(items, 'item-list');
}

function disconnectBrowseObserver() {
  if (browseObserver) { browseObserver.disconnect(); browseObserver = null; }
}

function disconnectClObserver() {
  if (clObserver) { clObserver.disconnect(); clObserver = null; }
}

function renderItemList(items, containerId) {
  const container = el(containerId);
  if (!container) return;

  // Non-browse containers (nonjav-list etc.): render everything at once
  if (containerId !== 'item-list') {
    const frag = document.createDocumentFragment();
    items.forEach((item, idx) => frag.appendChild(createItemCard(item, idx)));
    container.innerHTML = '';
    container.appendChild(frag);
    return;
  }

  // Browse: render in pages, load next page when sentinel scrolls into view
  disconnectBrowseObserver();
  browseOffset = 0;
  container.innerHTML = '';

  function appendNextPage() {
    const end  = Math.min(browseOffset + BROWSE_PAGE, items.length);
    const frag = document.createDocumentFragment();
    for (let i = browseOffset; i < end; i++) {
      frag.appendChild(createItemCard(items[i], i));
    }
    browseOffset = end;

    // Remove stale sentinel before appending new items
    const old = container.querySelector('.browse-sentinel');
    if (old) old.remove();

    container.appendChild(frag);

    if (browseOffset < items.length) {
      // Add a sentinel div that triggers the next page when it nears the viewport
      const sentinel = document.createElement('div');
      sentinel.className = 'browse-sentinel';
      container.appendChild(sentinel);

      browseObserver = new IntersectionObserver(entries => {
        if (entries[0].isIntersecting) {
          disconnectBrowseObserver();
          appendNextPage();
        }
      }, { rootMargin: '300px' });   // start loading 300px before sentinel is visible
      browseObserver.observe(sentinel);
    }
  }

  appendNextPage();
}

function createItemCard(item, idx = -1) {
  const isMarked      = markedItems.has(item.path);
  const isSelected    = selectedItem && selectedItem.path === item.path;
  const isMultiSelect = multiSelected.has(item.path);

  const card = document.createElement('div');
  card.className = 'item-card' +
    (isSelected    ? ' is-selected'      : '') +
    (isMarked      ? ' is-marked'        : '') +
    (isMultiSelect ? ' is-multi-selected': '');
  card.dataset.path = item.path;

  // Checkbox for multi-select
  const checkCol = document.createElement('div');
  checkCol.className = 'item-check-col';
  const checkbox = document.createElement('input');
  checkbox.type    = 'checkbox';
  checkbox.className = 'item-check';
  checkbox.checked = isMultiSelect;
  checkbox.addEventListener('click', e => {
    e.stopPropagation();
    handleMultiClick(item, idx, e.shiftKey);
  });
  checkCol.appendChild(checkbox);
  card.appendChild(checkCol);

  // Series badge
  const badge = document.createElement('div');
  badge.className = 'item-series-badge';
  const series = item.series || '?';
  badge.textContent = series.length > 7 ? series.slice(0, 7) + '…' : series;
  badge.style.background   = item.is_jav ? seriesColor(series, 0.2)  : 'rgba(100,100,120,0.25)';
  badge.style.color        = item.is_jav ? seriesColor(series, 1)    : '#7070a0';
  badge.style.border       = `1px solid ${item.is_jav ? seriesColor(series, 0.45) : 'transparent'}`;
  card.appendChild(badge);

  // Cover thumbnail (embedded from metadata)
  if (item.cover) {
    const thumb = document.createElement('img');
    thumb.className = 'item-thumb';
    thumb.src = item.cover;
    thumb.alt = '';
    thumb.loading = 'lazy';
    thumb.referrerPolicy = 'no-referrer';   // bypass hotlink protection on image hosts
    thumb.onerror = function() { this.style.display = 'none'; };
    card.appendChild(thumb);
  }

  // Info — show javbus title if available, folder name as fallback/tooltip
  const actressHtml = item.actresses?.length
    ? `<div class="item-actresses">` +
      item.actresses.slice(0, 3).map(a =>
        `<span class="actress-tag actress-link" data-actress="${esc(a)}" title="Browse ${esc(a)}">${esc(a)}</span>`
      ).join('') +
      (item.actresses.length > 3
        ? `<span class="actress-tag" title="${item.actresses.slice(3).map(a => esc(a)).join(', ')}">+${item.actresses.length - 3}</span>`
        : '') +
      `</div>`
    : '';

  const info = document.createElement('div');
  info.className = 'item-info';
  info.innerHTML = `
    <div class="item-bango">${esc(item.bango || '(no bango)')}</div>
    <div class="item-name" title="${esc(item.name)}">${esc(item.title || item.name)}</div>
    <div class="item-meta">${item.file_count} file(s) · ${item.video_count} video(s)</div>
    ${actressHtml}
  `;
  card.appendChild(info);

  // Size
  const sizeEl = document.createElement('div');
  sizeEl.className = 'item-size';
  sizeEl.textContent = item.total_size_human || '?';
  card.appendChild(sizeEl);

  // Mark button
  const markBtn = document.createElement('button');
  markBtn.className   = 'mark-btn' + (isMarked ? ' marked' : '');
  markBtn.textContent = isMarked ? '🗑️ Marked' : '🗑️ Mark';
  markBtn.onclick = e => {
    e.stopPropagation();
    toggleMark(item);
    const marked = markedItems.has(item.path);
    markBtn.className   = 'mark-btn' + (marked ? ' marked' : '');
    markBtn.textContent = marked ? '🗑️ Marked' : '🗑️ Mark';
    card.classList.toggle('is-marked', marked);
    if (selectedItem?.path === item.path) updateDetailMarkBtn();
  };
  card.appendChild(markBtn);

  card.addEventListener('click', () => openDetail(item));
  return card;
}


