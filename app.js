function toggleMark(item) {
  if (markedItems.has(item.path)) markedItems.delete(item.path);
  else markedItems.add(item.path);
  updateMarkedSidebar();
}

function toggleMarkFromDetail() {
  if (!selectedItem) return;
  toggleMark(selectedItem);
  updateDetailMarkBtn();
  const card = document.querySelector(`.item-card[data-path="${CSS.escape(selectedItem.path)}"]`);
  if (card) {
    const marked = markedItems.has(selectedItem.path);
    card.classList.toggle('is-marked', marked);
    const btn = card.querySelector('.mark-btn');
    if (btn) {
      btn.className   = 'mark-btn' + (marked ? ' marked' : '');
      btn.textContent = marked ? '🗑️ Marked' : '🗑️ Mark';
    }
  }
}

function updateMarkedSidebar() {
  const count = markedItems.size;
  const sidebar = el('marked-sidebar');
  el('marked-count').textContent = count;
  sidebar.classList.toggle('hidden', count === 0);
}

function clearMarked() {
  markedItems.clear();
  updateMarkedSidebar();
  document.querySelectorAll('.item-card.is-marked').forEach(c => {
    c.classList.remove('is-marked');
    const btn = c.querySelector('.mark-btn');
    if (btn) { btn.className = 'mark-btn'; btn.textContent = '🗑️ Mark'; }
  });
  updateDetailMarkBtn();
}

