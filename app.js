'use strict';


// ─────────────────────────────────────────────
// State
// ─────────────────────────────────────────────

let appData        = null;
let allItems       = [];
let filteredItems  = [];
let markedItems    = new Set();
let multiSelected  = new Set();   // paths currently multi-selected
let lastMultiIndex = -1;          // index in filteredItems of last Ctrl/Shift click
let currentView    = 'dashboard';
let selectedItem   = null;
let countChart     = null;
let sizeChart      = null;
let seriesSortKey  = 'count';
let seriesSortAsc  = false;


// ─────────────────────────────────────────────
// Bootstrap
// ─────────────────────────────────────────────

document.addEventListener('DOMContentLoaded', init);

function init() {
  if (window.__javData__) {
    appData = window.__javData__;
    onDataReady();
  } else {
    // data.js not loaded — user needs to run scan.py first
    el('loading').classList.add('hidden');
    el('error-msg').classList.remove('hidden');
  }
}

function reloadData() {
  window.location.reload();
}

function onDataReady() {
  allItems = appData.items || [];

  const s = appData.statistics || {};
  setText('scan-time',  fmtDate(appData.scan_time));
  setText('total-size', s.total_size_human || '?');
  setText('total-dirs', s.total_items || '?');

  const nonjav = allItems.filter(i => !i.is_jav).length;
  const badge = el('nonjav-badge');
  if (nonjav > 0) { badge.textContent = nonjav; badge.style.display = ''; }
  else { badge.style.display = 'none'; }

  el('loading').classList.add('hidden');
  showView(currentView || 'dashboard');
}


// ─────────────────────────────────────────────
// View management
// ─────────────────────────────────────────────

function hideAllViews() {
  document.querySelectorAll('.view').forEach(v => v.classList.add('hidden'));
}

function showView(name) {
  hideAllViews();
  currentView = name;
  lastMultiIndex = -1;   // indices are view-specific; reset on tab switch

  const viewEl = el(`view-${name}`);
  if (viewEl) viewEl.classList.remove('hidden');

  document.querySelectorAll('.nav-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.view === name);
  });

  if      (name === 'dashboard')  renderDashboard();
  else if (name === 'browse')     renderBrowse();
  else if (name === 'statistics') renderStatistics();
  else if (name === 'nonjav')     renderNonJav();
}


