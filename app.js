// Javbus URL slug differs by studio:
//   1PONDO-052611-102     → 052611_102   (underscore)
//   CARIBBEANCOM-102720-001 → 102720-001  (dash)
//   everything else       → bango as-is
function javbusSlug(bango) {
  let m = bango.match(/^1PONDO-(\d{6})-(\d{3})$/i);
  if (m) return `${m[1]}_${m[2]}`;
  m = bango.match(/^(?:CARIBBEANCOM|CARIBPR)-(\d{6})-(\d{3})$/i);
  if (m) return `${m[1]}-${m[2]}`;
  return bango;
}

function openJavbus(bango) {
  const overlay = el('javbus-overlay');
  const iframe  = el('javbus-iframe');
  const bangoEl = el('javbus-bango-label');
  const link    = el('javbus-newtab');

  const slug = javbusSlug(bango);
  const url  = `https://www.javbus.com/${encodeURIComponent(slug)}`;
  if (bangoEl) bangoEl.textContent = bango;
  if (link)    link.href = url;
  iframe.src = url;
  overlay.classList.remove('hidden');
}

function closeJavbus() {
  const overlay = el('javbus-overlay');
  const iframe  = el('javbus-iframe');
  if (!overlay) return;
  overlay.classList.add('hidden');
  iframe.src = '';   // stop loading
}


