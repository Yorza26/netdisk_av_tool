function renderDashboard() {
  if (!appData) return;
  const s = appData.statistics || {};

  setText('s-total',  s.total_items   || 0);
  setText('s-jav',    s.jav_count     || 0);
  setText('s-nonjav', s.non_jav_count || 0);
  setText('s-size',   s.total_size_human || '?');
  setText('s-series', Object.keys(s.series_count || {}).length);

  const countEntries = Object.entries(s.series_count || {}).slice(0, 20);
  renderBarChart('chart-count', countEntries.map(([k]) => k),
    countEntries.map(([, v]) => v), '# videos', countEntries.map(([k]) => k));

  const sizeData    = s.series_size || {};
  const sizeEntries = Object.entries(sizeData)
    .sort((a, b) => b[1].size - a[1].size).slice(0, 20);
  renderBarChart('chart-size', sizeEntries.map(([k]) => k),
    sizeEntries.map(([, v]) => +(v.size / 1e9).toFixed(1)), 'GB',
    sizeEntries.map(([k]) => k));
}

function renderBarChart(canvasId, labels, values, unit, colorKeys) {
  const canvas = el(canvasId);
  if (!canvas) return;

  const existing = Chart.getChart(canvas);
  if (existing) existing.destroy();

  // Fix explicit pixel size — prevents Chart.js responsive resize loop
  const w = canvas.parentElement.clientWidth || 500;
  const h = Math.max(260, labels.length * 28);
  canvas.width  = w;
  canvas.height = h;
  canvas.style.width  = w + 'px';
  canvas.style.height = h + 'px';

  new Chart(canvas, {
    type: 'bar',
    data: {
      labels,
      datasets: [{
        data:            values,
        backgroundColor: colorKeys.map(k => seriesColor(k, 0.65)),
        borderColor:     colorKeys.map(k => seriesColor(k, 1)),
        borderWidth: 1,
      }],
    },
    options: {
      animation: false,
      responsive: false,
      indexAxis: 'y',
      plugins: {
        legend: { display: false },
        tooltip: { callbacks: { label: ctx => ` ${ctx.raw} ${unit}` } },
      },
      scales: {
        x: { beginAtZero: true,
             ticks: { color: '#7070a0', font: { size: 11 } },
             grid:  { color: '#2a2a50' } },
        y: { ticks: { color: '#e0e0f0', font: { size: 11 } },
             grid:  { display: false } },
      },
    },
  });
}


