function openDetail(item) {
  selectedItem = item;
  document.querySelectorAll('.item-card').forEach(c =>
    c.classList.toggle('is-selected', c.dataset.path === item.path)
  );
  el('detail-panel').classList.remove('hidden');
  renderDetailShell(item);
}

function closeDetail() {
  el('detail-panel').classList.add('hidden');
  closeJavbus();
  selectedItem = null;
  document.querySelectorAll('.item-card.is-selected')
    .forEach(c => c.classList.remove('is-selected'));
}

function renderDetailShell(item) {
  const isMarked = markedItems.has(item.path);

  // Build metadata block from embedded fields (populated by scan.py via javbus)
  let metaHtml = '';
  if (item.is_jav && (item.cover || item.title || item.actresses?.length)) {
    const actressHtml = item.actresses?.length
      ? `<div class="jav-actresses">👤 ${item.actresses.map(a => `<span class="actress-tag">${esc(a)}</span>`).join('')}</div>`
      : '';
    metaHtml = `
      <div class="jav-info-card">
        ${item.cover ? `<img class="jav-cover" src="${esc(item.cover)}" alt="cover" loading="lazy" onerror="this.style.display='none'">` : ''}
        <div class="jav-meta">
          ${item.title ? `<div class="jav-title">${esc(item.title)}</div>` : ''}
          ${actressHtml}
        </div>
      </div>`;
  }

  el('detail-body').innerHTML = `
    <div class="detail-bango">${esc(item.bango || '(no bango)')}</div>

    ${metaHtml}

    <div class="detail-section-title">Files on disk</div>
    <div style="font-size:12px;color:var(--text-muted);margin-bottom:8px;">
      <a class="path-link" href="${localUrl(item.path)}" target="_blank" title="Open folder">📁 ${esc(item.name)}</a>
      &nbsp;·&nbsp; ${item.total_size_human} &nbsp;·&nbsp; ${item.file_count} file(s)
    </div>
    <div id="detail-files">${renderFileList(item.path, item.files)}</div>
    ${item.file_count > item.files.length
      ? `<div style="font-size:12px;color:var(--text-muted);margin-top:6px;">
           … and ${item.file_count - item.files.length} more in subfolders
         </div>` : ''}

    <div class="detail-actions">
      ${item.bango
        ? `<button class="detail-link-btn" onclick="openJavbus('${esc(item.bango)}')">
             🎬 View on Javbus
           </button>` : ''}
      <button class="detail-mark-btn ${isMarked ? 'unmark' : ''}" id="detail-mark-btn"
        onclick="toggleMarkFromDetail()">
        ${isMarked ? '↩️ Unmark' : '🗑️ Mark for Deletion'}
      </button>
    </div>
  `;
}

function renderFileList(folderPath, files) {
  if (!files?.length) return '<div class="muted">No files listed</div>';
  return files.map(f => {
    const url = localUrl(folderPath + '\\' + f.name);
    return `<div class="file-entry">
       <span>${fileIcon(f.ext)}</span>
       <a class="file-name path-link" href="${url}" target="_blank" title="${esc(f.name)}">${esc(f.name)}</a>
       <span class="file-size">${f.size_human || ''}</span>
     </div>`;
  }).join('');
}

// Convert a Windows path to a file:// URL
function localUrl(winPath) {
  return 'file:///' + winPath.replace(/\\/g, '/');
}

function fileIcon(ext) {
  const VIDEO = new Set(['.mp4','.mkv','.avi','.wmv','.mov','.m4v','.ts','.m2ts','.iso','.rmvb','.flv','.webm']);
  if (VIDEO.has(ext)) return '🎬';
  if (['.jpg','.jpeg','.png','.webp','.avif','.gif','.bmp'].includes(ext)) return '🖼️';
  if (ext === '.nfo') return '📄';
  if (['.srt','.ass','.ssa','.sub'].includes(ext)) return '💬';
  return '📎';
}

function updateDetailMarkBtn() {
  if (!selectedItem) return;
  const btn = el('detail-mark-btn');
  if (!btn) return;
  const marked = markedItems.has(selectedItem.path);
  btn.className   = 'detail-mark-btn' + (marked ? ' unmark' : '');
  btn.textContent = marked ? '↩️ Unmark' : '🗑️ Mark for Deletion';
}


